

package com.github._1c_syntax.mdclasses.jackson.designer;


import com.fasterxml.jackson.annotation.JsonProperty;

public enum FormElementOrigin {

    @JsonProperty("Source")
    SOURCE("Source"),
    @JsonProperty("AutoGenerated")
    AUTO_GENERATED("AutoGenerated"),
    @JsonProperty("AddedFromContext")
    ADDED_FROM_CONTEXT("AddedFromContext");
    private final String value;

    FormElementOrigin(String v) {
        value = v;
    }

    public String value() {
        return value;
    }

    public static FormElementOrigin fromValue(String v) {
        for (FormElementOrigin c : FormElementOrigin.values()) {
            if (c.value.equals(v)) {
                return c;
            }
        }
        throw new IllegalArgumentException(v);
    }

}
